package com.academicdashboard.backend.checklist;

import java.util.ArrayList;
import java.util.List;

import org.bson.types.ObjectId; //MongoDB ObjectId (AutoGenerated)
import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;
import org.springframework.data.mongodb.core.mapping.DocumentReference;

import lombok.AllArgsConstructor;
import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

@Document(collection = "checklist")
@Getter
@Setter
@EqualsAndHashCode
@NoArgsConstructor
@AllArgsConstructor
public class Checklist {

    @Id
    private ObjectId id; //MongoDB ObjectId
    
    //CheckList's Details
    private String listId; //Used for Queries (JNanoId)
    private String title;
    private List<Checkpoint> checkpoints; //Empty if isGroup = true

    //Is Group of Checklist
    private boolean isGroup;

    @DocumentReference
    private List<Checklist> children; //Empty if isGroup = false


    //Constructor: Individual Checklist is Created
    public Checklist(
            String listId,
            String title
        ) {

        this.listId = listId;
        this.title = title;
        this.checkpoints = new ArrayList<>();

        this.isGroup = false;
        this.children = new ArrayList<>(); //Empty ArrayList
    }

    //Constructor: Group of Checklist is Created
    public Checklist(
            String listId,
            String title,
            boolean isGroup
        ) {

        this.listId = listId;
        this.title = title;
        this.isGroup = isGroup;
        this.children = new ArrayList<>();

        this.checkpoints = new ArrayList<>(); //Empty ArrayList
    }
}
