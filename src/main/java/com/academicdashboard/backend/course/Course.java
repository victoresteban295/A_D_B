package com.academicdashboard.backend.course;

import java.util.List;

import org.bson.types.ObjectId; //MongoDB ObjectId (AutoGenerated)
import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;

import lombok.AllArgsConstructor;
import lombok.EqualsAndHashCode;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

@Document(collection = "course")
@Getter
@Setter
@EqualsAndHashCode
@NoArgsConstructor
@AllArgsConstructor
public class Course {

    @Id
    private ObjectId id; //MongoDB ObjectId
    
    //Course Details
    private String crsDept; //Course Department
    private String crsTitle; //Course Title
    private String crsNum; //Course Number
    private String crsSection; //Course Section

    //Professor Information
    private String instructor;
    private ObjectId instructorId;

    //Date, Time, & Lecture
    private String firstDate;
    private String lastDate;
    private String semester;
    private int schoolYr;
    private List<LectureHrs> lecture;

    //Data Relationships
    private List<ObjectId> students;
    private List<ObjectId> syllabusSection;
    private List<ObjectId> assignment;
    private List<ObjectId> test;
    private List<ObjectId> announcement;
}
